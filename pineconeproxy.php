function CallAPI($method, $APIKey, $url, $data = false)
{
  // show_debug("In CallAPI, the data is: " . $data  . "\n");
  show_debug("In CallAPI, the url is: " . $url  . "\n");
  show_debug("In CallAPI, the method is: " . $method  . "\n");
  show_debug("In CallAPI, the APIKey is: " . $APIKey  . "\n");

    $curl = curl_init();

    switch ($method)
    {
        case "GET":
        curl_setopt($curl, CURLOPT_HTTPGET, 1);
        break;

        case "POST":
            curl_setopt($curl, CURLOPT_POST, 1);
            // show_debug("In CallAPI, just before setting the CURLOPT_POSTFIELDS, the data is: " . $data . "\n" );

            if ($data)
                curl_setopt($curl, CURLOPT_POSTFIELDS, $data);
              show_debug("Just did the curl_setopt for CURLOPT_POSTFIELDS\n");
            break;
        case "PUT":
            curl_setopt($curl, CURLOPT_PUT, 1);
            break;
        default:
            if ($data)
                $url = sprintf("%s?%s", $url, http_build_query($data));
    }

    show_debug("Just before curl_setopt for CURLOPT_SSL_VERIFYPEER\n");

    curl_setopt($curl, CURLOPT_SSL_VERIFYPEER, false);
    curl_setopt($curl, CURLOPT_URL, $url);
    curl_setopt($curl, CURLOPT_RETURNTRANSFER, 1);
    curl_setopt($curl, CURLOPT_HTTPHEADER, [
      'Content-Type: application/json', 
      'Api-Key: ' . $APIKey,
      'Accept: */*',
      ]);

   show_debug("Just before curl_exec\n");
    $result = curl_exec($curl);

    curl_close($curl);

    //print("About to return");
    //print("\n Data is:\n".$data);

    return $result;
}

